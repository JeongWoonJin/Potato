package vo;

public class CategoryVO {
	private int c_lid;
	private int c_sid;
	private String c_name;

	public int getC_lid() {
		return c_lid;
	}

	public void setC_lid(int c_lid) {
		this.c_lid = c_lid;
	}

	public int getC_sid() {
		return c_sid;
	}

	public void setC_sid(int c_sid) {
		this.c_sid = c_sid;
	}

	public String getC_name() {
		return c_name;
	}

	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
}
